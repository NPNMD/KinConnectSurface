/**
 * Escapes HTML special characters to prevent XSS attacks.
 * @param unsafe - The string to escape
 * @returns The escaped string
 */
export const escapeHtml = (unsafe: string): string => {
  return unsafe
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#039;");
};

